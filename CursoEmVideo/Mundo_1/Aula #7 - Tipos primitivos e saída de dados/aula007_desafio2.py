# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raíz quadrada.

print('Escolha um número que direi o dobro, o triplo e a raíz quadrada!')
n = int(input('Número: '))
print('O número escolhido foi {}. \nO dobro é igual a {}. \nO triplo {}. \nE a raíz quadrada é igual a {:.2f}'.format(n, (n*2), (n*3), (n**(1/2))))

